/*
Primer ejercicio, declaración de variables y primer programa
 */
public class Ejercicio001 {
    public static void main(String[] args) {
        int x;
        int y;
        int resultado; //declaración de variables
        x = 55;
        y = 50;
        resultado = x + y;
        System.out.println("El resultado es: " + resultado);
    }
}
/*
public se puede usar en varios paquetes
private solo se puede usar en la clase usada
String es una cadena de caracteres
args es un array de cadenas de caracteres
variable es un espacio de memoria temporal que almacena un valor
x= 55; asignación de valor a la variable x (instrucción)
x== 55; comparación de valores
+ puede ser de suma o concatenación
 */

